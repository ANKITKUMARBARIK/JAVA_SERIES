public class fourth {
    public static void main(String args[]) {
        System.out.println("Hello");
        // LOOPS

        // while loop
        int i = 1;
        while (i <= 5) {
            System.out.println("while : " + i);
            i++;
        }

        // do-while loop
        int j = 1;
        do {
            System.out.println("do-while : " + j);
            j++;
        } while (j <= 5);

        // for loop
        for (int k = 1; k <= 5; k++) {
            System.out.println("for : " + k);
        }

    }
}
